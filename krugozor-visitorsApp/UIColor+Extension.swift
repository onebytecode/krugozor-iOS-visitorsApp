import UIKit

extension UIColor {
    struct CustomColors {
        static var first: UIColor  {
            return UIColor(red: 21/255, green: 180/255, blue: 241/255, alpha: 1)
        }
        static var second: UIColor {
            return UIColor(red: 21/255, green: 180/255, blue: 241/255, alpha: 0.2)
        }
        
        static var third: UIColor {
            return UIColor()
        }
    }
}
